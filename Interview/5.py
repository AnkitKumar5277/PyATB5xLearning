
# Using single quotes
name = 'Ankit'
print(name)

# Using double quotes
city = "Delhi"
print(city)

# Case where single quotes are helpful (string contains double quotes)
quote1 = 'He said, "Python is easy!"'
print(quote1)

# Case where double quotes are helpful (string contains single quotes)
quote2 = "It's a beautiful day"
print(quote2)

# If you want both single and double quotes inside, use triple quotes
quote3 = '''She said, "It's Python, not Java!"'''
print(quote3)
