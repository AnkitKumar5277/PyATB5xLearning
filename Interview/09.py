# Integer arithmetic
x = 10
y = 3
z = x + y   # z = 13
z = x - y   # z = 7
z = x * y   # z = 30
z = x // y  # z = 3 (integer division)
z = x % y   # z = 1 (remainder)

# Float arithmetic
x = 3.14
y = 1.5
z = x + y   # z = 4.64
z = x - y   # z = 1.64
z = x * y   # z = 4.71
z = x / y   # z = 2.0933...
