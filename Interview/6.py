# Creating variables
name = "Ankit"      # string type variable
age = 21            # integer type variable
height = 5.9        # float type variable
is_student = True   # boolean type variable

# Printing variables
print("Name:", name)
print("Age:", age)
print("Height:", height)
print("Are you a student?", is_student)

# You can change the value of a variable
age = 22
print("Updated Age:", age)

