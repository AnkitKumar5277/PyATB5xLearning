# Converting a string to an integer
x = "10"
y = int(x)
print(y)  # Output: 10

# Converting a float to an integer
x = 3.14
y = int(x)
print(y)  # Output: 3

# Converting an integer to a string
x = 42
y = str(x)
print(y)  # Output: "42"

# Converting a boolean to a string
x = True
y = str(x)
print(y)  # Output: "True"
